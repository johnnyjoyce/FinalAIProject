package gmit;

public class SearchAlgorithm {
	public String name;
	public int beamWidth;
	
	public String getName() 
	{
		return name;
	}
	
	public void setName(String name) 
	{
		this.name = name;
	}
	
	public int getBeamWidth() 
	{
		return beamWidth;
	}
	
	public void setBeamWidth(int beamWidth) 
	{
		this.beamWidth = beamWidth;
	}
}
